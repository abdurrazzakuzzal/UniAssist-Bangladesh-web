	<h3>Eligibility</h3>
	<p>Undergraduate Admission Eligibility</p>
	<p align="left">1.A minimum GPA of 2.5/2nd division/equivalent grade in SSC and HSC or equivalent public examinations. However, students with GPA 2.00 in any examination either SSC or HSC must have a minimum GPA of 6.00 totaling the GPA of both the examinations.<br>
2.Candidates must have passed University of London and Cambridge GCE ‘O’ Level in at least five subjects and ‘A’ Level in at least two subjects. Only the best five subjects in ‘O’ Level and best two subjects in ‘A’ Level will be considered. Out of these seven subjects, a candidate must have at least 4B’s or GPA of 4.00 in the four subjects and 3 C’s or GPA of 3.5 in the remaining three subjects. (in the scale of A=5, B=4, C=3, D=2 and E=1.<br>
3.Diploma in Engineering under the Bangladesh Technical Education Board (BTEB) or equivalent, with at least a second division or a minimum GPA of 2.5.<br>
4.For admission to BSc in CSE, BSc in EEE and BSc in Textile Engineering programs, students must have had Physics, Chemistry and Mathematics with satisfactory scores in HSC/A-Level or equivalent level. Candidates not having Mathematics or failed in Mathematics at HSC/A-levels must take a remedial course on Mathematics in addition to the courses required for the program.<br>
5.The students, who are sons/daughters of freedom fighters, will be eligible for getting admission if they have a minimum GPA of 5.00 totaling the GPA of SSC and HSC Examinations. Such students are requested to submit attested photocopies of (i) “Provisional Certificate” from Ministry of Liberation war affairs. (ii) Mukti Barta (iii) Gazette (iv) “National ID” of the freedom fighter. (v) Birth Certificate / Death Certificate of the Freedom Fighter. (vi)  Database of Freedom Fighters Information (vii) Government allowance book.<br></p>
