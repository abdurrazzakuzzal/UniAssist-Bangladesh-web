    <h3>Eligibility</h3>
    <p><bold>Undergraduate Program</bold><br></p>
    <p>a. Minimum GPA of 3.00 in both SSC and HSC Examinations. or<br>
b. Candidates must have passed University of London and Cambridge GCE ‘O’ Level in at least five subjects and ‘A’ Level in at least two subjects. Only the best five subjects in ‘O’ Level and best two subjects in ‘A’ Level will be considered. Out of these seven subjects, a candidate must have at least 4B’s or GPA of 4.00 in the four subjects and 3 C’s or GPA of 3.5 in the remaining three subjects. (in the scale of A=5, B=4, C=3, D=2 and E=1
or<br>
c. American High School Diploma, AND<br>
d. Acceptable EWU Admission Test Score.<br>
e. Total GPA of 5.00 in both SSC and HSC Examinations for the children of Freedom Fighter.<br>
f. The final selection of candidates for admission in the Undergraduate Programs at EWU will be based on the Admission Test scores obtained with 75% from admission test, 10% from SSC/O-level and 15% from HSC/A-level.<br>

</p>
<p><bold>Admission Test Waiver</bold><br></p>
	<p>
		The candidates securing a minimum total score of 1500 in SAT (considering Critical Reading, Math and Writing). The candidates seeking admission in the Faculty of Sciences and Engineering must have minimum CGPA 3.5 in Math and Physics separately in HSC/A level examinations in addition to above mentioned SAT scorer to get waiver from the Admission Test. Students who have completed a two-year Bachelor’s degree from a recognized university can apply for admission into the four-year undergraduate program. However, EWU will consider applications for credit transfer only in cases where previous academic performance meets EWU degree requirements.<br>


	</p>
	<p><bold>Requirements for foreign students</bold><br></p>
	<p>Foreign Students particularly who come from other systems like US High School Diploma, Indian/Nepalese system etc. (not from SSC/HSC or O/A Level etc. system) the admission eligibility will be as follows:<br>

1.SAT score of 1100 or<br>
2.Pass with at least upper 50% marks/grade of their own education system.<br>
3.Foreign students fulfilling the admission eligibility need not appear at the Admission Test.<br>
4.An equivalence committee will assess and recommend for satisfactory grade for the applicants who seek admission in EWU with US High School Diploma or who come from other systems. A committee will assess and recommend for waiver of Admission Test, Scholarship etc. (if applicable) for foreign students and the students from other systems.<br>

</p>
