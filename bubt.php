	<p>
		Bangladesh University of Business and Technology (Bengali: বাংলাদেশ ইউনিভার্সিটি অফ বিজনেস অ্যান্ড টেকনোলজি) or BUBT is one of the eight private universities to receive the green Signal from the government, located in Mirpur, Dhaka, Bangladesh. The university was established under the Private University Act 1992.[1] BUBT is regulated by the Bangladesh University Grants Commission (UGC).

BUBT was founded in 2003 under the authority of the Private University Act and was approved by the University Grants Commission (UGC)[2] and the Ministry of Education, Government of Bangladesh. It was modeled after North American universities. It started its operation in the Dhaka Commerce College campus, the founder organization of BUBT. It is running its programs in its own campus at Rupnagar R/A, Mirpur. Classes are also held at Dhaka Commerce College campus. In 2011 UGC has ranked BUBT as one of the best eight private universities. Around 1,000 students were awarded with their graduate and post graduate degrees in the first convocation this year (2011). BUBT is a member of the Association of Commonwealth Universities.
